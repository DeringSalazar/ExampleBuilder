package Builder;

import Cars.CarType;
import Cars.Manual;
import Components.Engine;
import Components.Transmission;
import Components.TripComputer;
import Components.GPSNavigator;

/*
A diferencia de otros patrones de creación, Builder puede construir productos no relacionados,
que no tienen la interfaz común.
 * En este caso construimos un manual de usuario para un automóvil, siguiendo los mismos pasos que
construyó un auto. Esto permite producir manuales para modelos de automóviles específicos,
configurado con diferentes características.
*/

public class CarManualBuilder implements Builder {
    private CarType type;
    private int seats;
    private Engine engine;
    private Transmission transmission;
    private TripComputer tripComputer;
    private GPSNavigator gpsNavigator;

    @Override
    public void setCarType(CarType type) {
        this.type = type;
    }

    @Override
    public void setSeats(int seats) {
        this.seats = seats;
    }

    @Override
    public void setEngine(Engine engine) {
        this.engine = engine;
    }

    @Override
    public void setTransmission(Transmission transmission) {
        this.transmission = transmission;
    }

    @Override
    public void setTripComputer(TripComputer tripComputer) {
        this.tripComputer = tripComputer;
    }

    @Override
    public void setGPSNavigator(GPSNavigator gpsNavigator) {
        this.gpsNavigator = gpsNavigator;
    }

    public Manual getResult() {
        return new Manual(type, seats, engine, transmission, tripComputer, gpsNavigator);
    }
}
